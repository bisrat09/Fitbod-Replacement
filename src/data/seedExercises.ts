const COMPOUND_RE =
  /\b(press|squat|deadlift|row|pull[ -]?up|pullup|chin[ -]?up|chinup|lunge|thrust|dip\b|dips\b|clean\b|snatch|jerk|push[ -]?up|pushup|thruster|muscle[ -]?up|step[ -]?up|step up|swing|good\s?morning|burpee|pulldown|pull[ -]?down|rack pull|inverted|turkish|wall ball|tire flip|sled\b|bear crawl|crawl|pistol)/i;

const NOT_COMPOUND_RE =
  /\b(raise|curl|extension|fly\b|kickback|crunch|plank|sit[ -]?up|shrug|rollout|roll out|flutter|scissor|dead bug|bird dog|v-up|v-sit|l sit|superman|windmill|windshield|vacuum|pike\b|halo|rotation\b|twist\b|circle|stretch|foam roll|toes to bar)\b/i;

function classify(name: string): 0 | 1 {
  if (ISOLATION_OVERRIDES.has(name)) return 0;
  // Check compound patterns first (press, squat, etc.)
  if (COMPOUND_RE.test(name)) return 1;
  return 0;
}

